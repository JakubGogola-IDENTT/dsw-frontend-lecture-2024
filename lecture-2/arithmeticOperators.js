let x = 10;
let y = 3;

// Addition
console.log(x + y); // 13

// Subtraction
console.log(x - y); // 7

// Multiplication
console.log(x * y); // 30

// Division
console.log(x / y); // 3.3333...

// Modulus (remainder)
console.log(x % y); // 1

// Exponentiation
console.log(x ** y); // 1000

// Increment (postfix)
console.log(x++); // 10;
console.log(x); // 11

// Increment (prefix)
console.log(++x); // 12
console.log(x); // 12

// Decrement (postfix)
console.log(y--); // 3
console.log(y); // 2

// Decrement (prefix)
console.log(--y); // 1
console.log(y); // 1
