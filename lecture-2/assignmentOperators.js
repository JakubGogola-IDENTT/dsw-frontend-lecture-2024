let a = 5;

// Basic assignment
a = 10;
console.log(a); // 10

// Addition assignment
a += 5; // Equivalent to: a = a + 5
console.log(a); // 15

// Subtraction assignment
a -= 2; // Equivalent to: a = a - 2
console.log(a); // 13

// Multiplication assignment
a *= 3; // Equivalent to: a = a * 3
console.log(a); // 39

// Division assignment
a /= 3; // Equivalent to: a = a / 3
console.log(a); // 13

// Modulus assignment
a %= 4; // Equivalent to: a = a % 4
console.log(a); // 1

// Exponentiation assignment
a **= 3; // Equivalent to: a = a ** 3
console.log(a); // 1 (1^3 is still 1)
